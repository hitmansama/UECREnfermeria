    void cargarEstudiantes() {
        DefaultComboBoxModel cbPersonas = new DefaultComboBoxModel();
        Persona auxPersona = new Persona();
        auxPersona.setNombres("");
        auxPersona.setApellidos("");
        cbPersonas.addElement(auxPersona);
        List<Persona> lPersonas = hPersona.obtenerPersonas();
        if (lPersonas != null) {
            Iterator it = lPersonas.iterator();
            while (it.hasNext()) {
                Persona per = (Persona) it.next();
                cbPersonas.addElement(per);
            }
            jcbEstudiante.setModel(cbPersonas);

        }
        AutoCompleteDecorator.decorate(jcbEstudiante);
    }

    void cargarRepresentantes() {
        List<Representante> ls = hRepresentante.obtenerRepresentantes();
        if (ls != null) {
            DefaultComboBoxModel cmod = new DefaultComboBoxModel();
            Representante rep = new Representante();
            rep.setNombres("");
            rep.setApellidos("");
            cmod.addElement(rep);
            Iterator it = ls.iterator();
            while (it.hasNext()) {
                cmod.addElement((Representante) it.next());
            }
            jcbRepresentante.setModel(cmod);
            AutoCompleteDecorator.decorate(jcbRepresentante);
        }
    }

    void cargarElectivos() {
        DefaultComboBoxModel cmod = new DefaultComboBoxModel();
        cmod.addElement(new Electivo("-Seleccione", ""));
        List<Electivo> ls = hElectivo.obtenerElectivos();
        if (ls != null) {
            Iterator it = ls.iterator();
            while (it.hasNext()) {
                cmod.addElement((Electivo) it.next());
            }
        }
        cmod.addElement(new Electivo("-Nuevo", ""));
        jcbElectivo.setModel(cmod);
    }

