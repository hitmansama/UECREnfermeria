    private void cargarPersonas() {
        List<Persona> personas = hPersona.obtenerPersonas();
        Iterator it = personas.iterator();
        DefaultComboBoxModel modelo = new DefaultComboBoxModel();
        Persona aux = new Persona();
        aux.setNombres("");
        aux.setApellidos("");
        modelo.addElement(aux);
        for (Iterator<Persona> iterator = personas.iterator(); iterator.hasNext();) {
            Persona next = iterator.next();
            modelo.addElement(next);
        }
        jcbAlumnos.setModel(modelo);
        AutoCompleteDecorator.decorate(jcbAlumnos);
    }

    private void cargarVacunas() {
        jchHpv01.setSelected(persona.getHpv01() == 1);
        jchHpv02.setSelected(persona.getHpv02() == 1);
        jchSrp01.setSelected(persona.getSrp01() == 1);
        jchSrp02.setSelected(persona.getSrp02() == 1);
        jchInfluencia.setSelected(persona.getInfluencia() == 1);
    }

    public void actualizarVacunas() {
        persona.setHpv01(toByte(jchHpv01.isSelected()));
        persona.setHpv02(toByte(jchHpv02.isSelected()));
        persona.setSrp01(toByte(jchSrp01.isSelected()));
        persona.setSrp02(toByte(jchSrp02.isSelected()));
        persona.setInfluencia(toByte(jchInfluencia.isSelected()));
        if (hPersona.editarPersona(persona)) {
            Herramientas.MensajeInfo("Vacunas actualizadas");
            this.dispose();
        }
    }

